/**
 * 构建包含当前 stage、profile 和 scenario 的动态系统提示词
 */
export function buildDynamicSystemPrompt(
  stage: string,
  profileSummary: string,
  scenario: string = 'F'
): string {
  let prompt = scenario === 'C' ? XIAOGENG_SYSTEM_PROMPT_C : XIAOGENG_SYSTEM_PROMPT;

  prompt += `\n\n# 当前对话状态\n`;
  prompt += `当前阶段：${stage}\n`;
  prompt += `已有档案：${profileSummary}\n`;

  if (stage === 'A') {
    prompt += `\n你现在处于A阶段（倾听者）。目标：拿到B1（真实处境）+ B2（真实痛点）。记住：缺哪个B就往哪个B追。`;
  } else if (stage === 'B') {
    prompt += `\n你现在处于B阶段（澄清者）。目标：拿到B3（可衡量改变信号）+ B4（现实约束）。她前面说过B1和B2已经清楚了，现在专心帮她看清想去的地方。`;
  } else if (stage === 'S') {
    prompt += `\n你现在处于S阶段（顾问）。B1-B4已全部拿到，接下来由系统生成方案包。你负责用温润笃定的语气把方案交付给她，收口三件事：方案已存档 / 30天回访 / 她不是一个人。`;
  }

  return prompt;
}
